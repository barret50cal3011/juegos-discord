package Client;
/*
 * @author Lele
 * Clase auxiliar para hacer puntos.
 */
public class Point
{
	/*
	 * Metodo constructor de un punto
	 * YO SE QUE JAVA TIENE UNA CLASE PUNTO, OKAY!?
	 */
    public Point(double x, double y)
    {
        this.x = x;
        this.y = y;
    }
    public final double x;
    public final double y;
}





